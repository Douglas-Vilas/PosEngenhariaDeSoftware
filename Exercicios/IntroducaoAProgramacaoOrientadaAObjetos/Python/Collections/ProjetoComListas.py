# Cria uma lista chamada 'linguagens'
linguagens = ["Java", "CSharp", "Python", "JavaScript"]

# Imprime a lista 'linguagens'
print("Lista: ", linguagens)

# Cria uma variável 'arr' que é igual à lista 'linguagens'
arr = linguagens
print("Array: ")

# Loop que percorrer cada elemento da lista 'arr'
for item in arr:
    print(item + ", ")